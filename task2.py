'''
N школьников делят K яблок поровну, не делящийся остаток остается в корзинке.
Сколько яблок достанется каждому школьнику?
'''
number = input()  # не меньше 1 и не больше 9
print('   _~_    ' * int(number))
print('  (o o)   ' * int(number))
print(' /  V  \\  ' * int(number))
print('/(  _  )\\ ' * int(number))
print('  ^^ ^^   ' * int(number))